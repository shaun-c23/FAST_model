using namespace std;

struct node {
    int num;
    double x;
    double y;
    double z;
    double T;
    double peakT;
    double MP;
};

node make_node(int i, double x, double y, double z, double T, double MP) {
    node node_ij;
    node_ij.num = i;
    node_ij.x = x;
    node_ij.y = y;
    node_ij.z = z;
    node_ij.T = T;
    node_ij.MP = MP;
    return node_ij;
}
